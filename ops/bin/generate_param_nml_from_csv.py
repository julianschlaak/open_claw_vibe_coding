#!/usr/bin/env python3
"""
Generate mhm_parameter.nml from recommended_params.csv

Usage: python generate_param_nml_from_csv.py <input_csv> <output_nml>

This script reads the recommended parameter values from a CSV file
(generated by the DDS analysis dashboard) and creates a valid
mhm_parameter.nml file for mHM simulations.
"""

import sys
import pandas as pd
import numpy as np
from pathlib import Path

def read_template_nml(template_path):
    """Read existing mhm_parameter.nml as template."""
    with open(template_path, 'r') as f:
        content = f.read()
    return content

def parse_csv_to_params(csv_path):
    """
    Read recommended_params.csv and extract parameter values.
    
    Returns dict: {param_id: value}
    """
    df = pd.read_csv(csv_path)
    
    params = {}
    for _, row in df.iterrows():
        param_id = int(row['param_id'])
        
        # Handle CATCHMENT_SPECIFIC recommendations
        if pd.isna(row['recommended_value']):
            print(f"⚠️  Param P{param_id:02d}: {row['recommendation']} - using default value")
            # Use median from other catchments or default
            params[param_id] = None  # Mark for manual review
        else:
            params[param_id] = float(row['recommended_value'])
    
    return params

def generate_nml(params, template_path, output_path):
    """
    Generate mhm_parameter.nml from parameter values.
    
    Args:
        params: Dict of {param_id: value}
        template_path: Path to existing mhm_parameter.nml (for structure)
        output_path: Output file path
    """
    # Read template
    template = read_template_nml(template_path)
    
    # Replace parameter values in template
    lines = template.split('\n')
    new_lines = []
    
    for line in lines:
        # Check if line contains parameter definition
        if line.strip().startswith('param_') and '=' in line:
            # Extract param id
            parts = line.split('=')
            param_name = parts[0].strip()
            
            # Extract param id from name (e.g., "param_01" → 1)
            try:
                param_id = int(param_name.split('_')[1])
                
                if param_id in params:
                    value = params[param_id]
                    if value is not None:
                        # Replace value
                        new_line = f"{param_name} = {value:.6f}"
                        new_lines.append(new_line)
                        print(f"✅ P{param_id:02d}: {value:.6f}")
                    else:
                        # Keep original with warning
                        new_lines.append(line)
                        print(f"⚠️  P{param_id:02d}: kept original (CATCHMENT_SPECIFIC)")
                else:
                    # Param not in CSV, keep original
                    new_lines.append(line)
            except (IndexError, ValueError):
                # Not a standard param_XX line, keep original
                new_lines.append(line)
        else:
            new_lines.append(line)
    
    # Write output
    with open(output_path, 'w') as f:
        f.write('\n'.join(new_lines))
    
    print(f"\n✅ Generated: {output_path}")

def main():
    if len(sys.argv) < 3:
        print("Usage: python generate_param_nml_from_csv.py <input_csv> <output_nml>")
        print("")
        print("Arguments:")
        print("  input_csv  - Path to recommended_params.csv")
        print("  output_nml - Path to output mhm_parameter.nml")
        print("")
        print("Example:")
        print("  python generate_param_nml_from_csv.py recommended_params.csv nml/mhm_parameter.nml")
        sys.exit(1)
    
    csv_path = Path(sys.argv[1])
    output_path = Path(sys.argv[2])
    
    # Check input file
    if not csv_path.exists():
        print(f"❌ Error: Input file not found: {csv_path}")
        sys.exit(1)
    
    print(f"📊 Reading: {csv_path}")
    params = parse_csv_to_params(csv_path)
    
    print(f"📝 Found {len(params)} parameters")
    print("")
    
    # Use template from first catchment (Parthe)
    # User should specify template path or use default
    template_dir = Path(csv_path.parent).parent / "code" / "mhm_re_crit" / "runs" / "parthe_0p0625" / "nml"
    template_path = template_dir / "mhm_parameter.nml"
    
    if template_path.exists():
        print(f"📋 Using template: {template_path}")
        generate_nml(params, template_path, output_path)
    else:
        print(f"❌ Template not found: {template_path}")
        print("   Please specify template path as third argument:")
        print("   python generate_param_nml_from_csv.py <csv> <output> <template>")
        sys.exit(1)

if __name__ == "__main__":
    main()
